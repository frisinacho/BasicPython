# Sum
print(5 + 2)

# Subtraction
print(5 - 2)

# Multiplication
print(5 * 2)

# Division -> Natural numbers
print(5 / 2)

# Module
print(5 % 2)

# Greater than
print(5 > 2)  # True

# Less than
print(5 < 2)  # False

# Larger operations
print(15 / 4 * 2)  # (15/4) = 3 | 3 * 2 = 6
print(16 / 4 * 2)  # (16/4) = 4 | 4 * 2 = 8

# How to solve it?
print(16 / (4 * 2))

# Concatenating Strings and Operations
